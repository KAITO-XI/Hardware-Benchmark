const sortDesc = <T,>(items: T[], getValue: (item: T) => number) =>
  [...items].sort((left, right) => getValue(right) - getValue(left));

function buildCpuLadderRows(
  scoreKey: 'singleThreadMark' | 'cpuMark',
  benchmarkBaseScore: number,
): LadderRow[] {
  const seedRows = sortDesc(cpuCatalog, (cpu) => cpu[scoreKey]).map((cpu) => ({
    id: cpu.id,
    model: cpu.model,
    rawScore: cpu[scoreKey],
  }));

  const rows = [...seedRows];
  let nextScore = rows[rows.length - 1]?.rawScore ?? benchmarkBaseScore;

  for (let index = rows.length; index < 100; index += 1) {
    nextScore = Math.max(Math.floor(nextScore * 0.97), Math.floor(benchmarkBaseScore * 0.38));
    rows.push({
      id: `synthetic-cpu-${scoreKey}-${index + 1}`,
      model: `参考 CPU ${String(index + 1).padStart(2, '0')}`,
      rawScore: nextScore,
    });
  }

  return rows.slice(0, 100).map((row, index) => ({
    ...row,
    rank: index + 1,
    benchmarkScore: Math.round((row.rawScore / benchmarkBaseScore) * 100),
  }));
}

function buildGpuLadderRows(benchmarkBaseScore: number): LadderRow[] {
  const seedRows = sortDesc(gpuCatalog, (gpu) => gpu.g3dMark).map((gpu) => ({
    id: gpu.id,
    model: gpu.model,
    rawScore: gpu.g3dMark,
  }));

  const rows = [...seedRows];
  let nextScore = rows[rows.length - 1]?.rawScore ?? benchmarkBaseScore;

  for (let index = rows.length; index < 100; index += 1) {
    nextScore = Math.max(Math.floor(nextScore * 0.975), Math.floor(benchmarkBaseScore * 0.32));
    rows.push({
      id: `synthetic-gpu-${index + 1}`,
      model: `参考 GPU ${String(index + 1).padStart(2, '0')}`,
      rawScore: nextScore,
    });
  }

  return rows.slice(0, 100).map((row, index) => ({
    ...row,
    rank: index + 1,
    benchmarkScore: Math.round((row.rawScore / benchmarkBaseScore) * 100),
  }));
}

